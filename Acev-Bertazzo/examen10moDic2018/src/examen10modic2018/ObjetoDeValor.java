public class ObjetoDeValor {
    private String nombre;
    private double valor;
    
    public ObjetoDeValor(String nombre, double valor) {
        this.nombre = nombre;
        this.valor = valor;
        
    }

    public String getNombre() {
        return nombre;
    }

    public double getValor() {
        return valor;
    }
    
    
}
